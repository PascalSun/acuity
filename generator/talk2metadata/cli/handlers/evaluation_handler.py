    def _evaluation_result_from_json(
        self,
        mode_name: str,
        payload: Dict[str, Any],
        *,
        file_timestamp: Optional[str] = None,
    ) -> EvaluationResult:
        """Rebuild an EvaluationResult from a saved JSON payload.

        Args:
            mode_name: Name of the mode this result belongs to.
            payload: Per-question dict from the ``detailed_results`` section.
            file_timestamp: Fallback ISO timestamp (from the file-level
                ``timestamp`` field) used when the per-result ``evaluated_at``
                is missing (backward compat with older JSONs).
        """
        retrieval = payload.get("retrieval", {}) or {}
        sql_block = payload.get("sql", {}) or {}
        difficulty_block = payload.get("difficulty", {}) or {}

        expected_ids = set(str(x) for x in retrieval.get("expected_row_ids", []) or [])
        predicted_ids = set(
            str(x) for x in retrieval.get("predicted_row_ids", []) or []
        )

        metrics = sql_block.get("metrics", {}) or {}

        # UID: prefer per-result uid; fallback to hash of question text
        qa_uid = payload.get("uid")
        if not qa_uid:
            import hashlib

            question_text = str(payload.get("question", ""))
            qa_uid = hashlib.sha256(question_text.encode("utf-8")).hexdigest()[:16]

        # Timestamp: prefer per-result evaluated_at; fallback to file-level
        evaluated_at = payload.get("evaluated_at") or file_timestamp

        return EvaluationResult(
            question=str(payload.get("question", "")),
            expected_row_ids=expected_ids,
            predicted_row_ids=predicted_ids,
            correct=bool(retrieval.get("hit", False)),
            precision=float(retrieval.get("row_precision", 0.0) or 0.0),
            recall=float(retrieval.get("row_recall", 0.0) or 0.0),
            f1=float(retrieval.get("row_f1", 0.0) or 0.0),
            mode_name=mode_name,
            strategy=payload.get("strategy"),
            difficulty_score=difficulty_block.get("score"),
            generated_sql=sql_block.get("generated_sql"),
            expected_sql=sql_block.get("expected_sql"),
            error_message=sql_block.get("error_message"),
            result_count=int(sql_block.get("result_count", 0) or 0),
            latency_ms=float(payload.get("latency_ms", 0.0) or 0.0),
            qa_uid=qa_uid,
            evaluated_at=evaluated_at,
            exact_match_score=float(metrics.get("exact_match", 0.0) or 0.0),
            execution_accuracy=float(metrics.get("execution_accuracy", 0.0) or 0.0),
            valid_efficiency_score=float(
                metrics.get("valid_efficiency_score", 0.0) or 0.0
            ),
            component_matching_f1=float(
                metrics.get("component_matching_f1", 0.0) or 0.0
            ),
            soft_f1=float(metrics.get("soft_f1", 0.0) or 0.0),
            tsed=float(metrics.get("tsed", 0.0) or 0.0),
            sqam=float(metrics.get("sqam", 0.0) or 0.0),
            metric_details=metrics,
        )

    def _build_summary_from_results(
        self, mode_name: str, results: List[EvaluationResult]
    ) -> ModeEvaluationSummary:
        """Aggregate a list of EvaluationResult objects into a summary."""
        classifier = DifficultyClassifier()

        def _tier_for_result(result: EvaluationResult) -> str:
            if result.difficulty_score is not None:
                if result.difficulty_score < 1.0:
                    return "easy"
                if result.difficulty_score < 2.0:
                    return "medium"
                if result.difficulty_score < 3.0:
                    return "hard"
                return "expert"
            if result.strategy:
                return classifier.get_tier(result.strategy)
            return "unknown"

        def _aggregate(group_results: List[EvaluationResult]) -> Dict[str, Any]:
            group_total = len(group_results)
            exact = sum(1 for r in group_results if r.is_exact_match)
            correct = sum(1 for r in group_results if r.correct)

            total_expected = sum(len(r.expected_row_ids) for r in group_results)
            total_predicted = sum(len(r.predicted_row_ids) for r in group_results)
            total_intersection = sum(r.intersection_size for r in group_results)

            micro_precision = (
                total_intersection / total_predicted if total_predicted > 0 else 0.0
            )
            micro_recall = (
                total_intersection / total_expected if total_expected > 0 else 0.0
            )
            micro_f1 = (
                2 * micro_precision * micro_recall / (micro_precision + micro_recall)
                if (micro_precision + micro_recall) > 0
                else 0.0
            )
            hit_rate = correct / group_total if group_total > 0 else 0.0
            exact_match_rate = exact / group_total if group_total > 0 else 0.0

            macro_precision = (
                sum(r.precision for r in group_results) / group_total
                if group_total > 0
                else 0.0
            )
            macro_recall = (
                sum(r.recall for r in group_results) / group_total
                if group_total > 0
                else 0.0
            )
            macro_f1 = (
                sum(r.f1 for r in group_results) / group_total
                if group_total > 0
                else 0.0
            )

            sql_group = [r for r in group_results if r.generated_sql and r.expected_sql]
            sql_n = len(sql_group)
            sql_coverage = (sql_n / group_total) if group_total > 0 else 0.0

            sql_metrics = {
                "evaluated_questions": sql_n,
                "coverage": sql_coverage,
                "avg_exact_match": (
                    sum(r.exact_match_score for r in sql_group) / sql_n
                    if sql_n
                    else 0.0
                ),
                "avg_execution_accuracy": (
                    sum(r.execution_accuracy for r in sql_group) / sql_n
                    if sql_n
                    else 0.0
                ),
                "avg_valid_efficiency_score": (
                    sum(r.valid_efficiency_score for r in sql_group) / sql_n
                    if sql_n
                    else 0.0
                ),
                "avg_component_matching_f1": (
                    sum(r.component_matching_f1 for r in sql_group) / sql_n
                    if sql_n
                    else 0.0
                ),
                "avg_soft_f1": (
                    sum(r.soft_f1 for r in sql_group) / sql_n if sql_n else 0.0
                ),
                "avg_tsed": sum(r.tsed for r in sql_group) / sql_n if sql_n else 0.0,
                "avg_sqam": sum(r.sqam for r in sql_group) / sql_n if sql_n else 0.0,
            }

            return {
                "counts": {
                    "total_questions": group_total,
                    "exact_matches": exact,
                    "hit_questions": correct,
                },
                "retrieval_metrics": {
                    "hit_rate": hit_rate,
                    "exact_match_rate": exact_match_rate,
                    "micro_precision": micro_precision,
                    "micro_recall": micro_recall,
                    "micro_f1": micro_f1,
                    "macro_precision": macro_precision,
                    "macro_recall": macro_recall,
                    "macro_f1": macro_f1,
                    "expected_rows": int(total_expected),
                    "predicted_rows": int(total_predicted),
                    "correct_rows": int(total_intersection),
                },
                "sql_metrics": sql_metrics,
            }

        aggregate_all = _aggregate(results)
        total = aggregate_all["counts"]["total_questions"]
        exact_matches = aggregate_all["counts"]["exact_matches"]
        hit_questions = aggregate_all["counts"]["hit_questions"]

        retrieval_metrics = RetrievalMetricsSummary(
            **aggregate_all["retrieval_metrics"]
        )
        sql_metrics = SQLMetricsSummary(**aggregate_all["sql_metrics"])

        # Calculate latency metrics
        latencies = [r.latency_ms for r in results]
        mean_latency = statistics.mean(latencies) if latencies else 0.0
        median_latency = statistics.median(latencies) if latencies else 0.0
        min_latency = min(latencies) if latencies else 0.0
        max_latency = max(latencies) if latencies else 0.0
        p95_latency = (
            statistics.quantiles(latencies, n=20)[18]
            if len(latencies) >= 20
            else (max(latencies) if latencies else 0.0)
        )
        p99_latency = (
            statistics.quantiles(latencies, n=100)[98]
            if len(latencies) >= 100
            else (max(latencies) if latencies else 0.0)
        )

        latency_metrics = LatencyMetricsSummary(
            mean_ms=mean_latency,
            median_ms=median_latency,
            min_ms=min_latency,
            max_ms=max_latency,
            p95_ms=p95_latency,
            p99_ms=p99_latency,
        )

        breakdown_by_strategy: Dict[str, Dict[str, Any]] = {}
        strategies = sorted({r.strategy for r in results if r.strategy})
        for strategy in strategies:
            strategy_results = [r for r in results if r.strategy == strategy]
            if not strategy_results:
                continue
            breakdown_by_strategy[strategy] = _aggregate(strategy_results)

        breakdown_by_tier: Dict[str, Dict[str, Any]] = {}
        tiers = ["easy", "medium", "hard", "expert", "unknown"]
        for tier in tiers:
            tier_results = [r for r in results if _tier_for_result(r) == tier]
            if not tier_results:
                continue
            breakdown_by_tier[tier] = _aggregate(tier_results)

        return ModeEvaluationSummary(
            mode_name=mode_name,
            total_questions=total,
            exact_matches=exact_matches,
            hit_questions=hit_questions,
            retrieval_metrics=retrieval_metrics,
            sql_metrics=sql_metrics,
            latency=latency_metrics,
            breakdown_by_strategy=breakdown_by_strategy,
            breakdown_by_tier=breakdown_by_tier,
            detailed_results=results,
        )

    def _load_domain_config(self) -> Dict[str, Any]:
        candidate_paths: List[Path] = []
        run_id = self.config.get("run_id")
        candidate_paths.extend(
            [
                Path("domain_config.yml"),
                Path(f"data/{run_id}/raw/domain_config.yml"),
            ]
        )

        for config_path in candidate_paths:
            if config_path.exists():
                try:
                    with open(config_path, "r", encoding="utf-8") as f:
                        parsed = yaml.safe_load(f) or {}
                    domain = (
                        parsed.get("domain", {}) if isinstance(parsed, dict) else {}
                    )
                    if isinstance(domain, dict):
                        logger.info(f"Loaded domain config from: {config_path}")
                        return domain
                except Exception as e:
                    logger.warning(
                        f"Failed to load domain config from {config_path}: {e}"
                    )

        return {}

    def _get_id_fields_priority(self, domain_config: Dict[str, Any]) -> List[str]:
        for key in ("id_fields_priority", "primary_key_fields", "primary_key_columns"):
            value = domain_config.get(key)
            if isinstance(value, list) and value:
                return [str(v).strip().lower() for v in value if str(v).strip()]
        return []

    def load_qa_pairs(
        self, qa_path: Optional[Path] = None, run_id: Optional[str] = None
    ) -> List[QAPair]:
        """Load QA pairs from JSON file.

        Args:
            qa_path: Optional path to QA pairs JSON file
            run_id: Optional run ID

        Returns:
            List of QAPair objects

        Raises:
            FileNotFoundError: If QA pairs file not found
        """
        if not qa_path:
            # Check config for qa_path override
            eval_config = self.config.get("evaluation", {})
            config_qa_path = eval_config.get("qa_path")
            if config_qa_path:
                qa_path = Path(config_qa_path)
            else:
                qa_dir = get_qa_dir(run_id or self.config.get("run_id"), self.config)
                qa_path = qa_dir / "qa_pairs.json"

        qa_path = Path(qa_path)
        if qa_path.name == "qa_pairs.json":
            qa_dir = qa_path.parent
            candidate_files = sorted(qa_dir.glob("qa_*/qa_pairs.json"))
            if candidate_files:
                merged_pairs: List[QAPair] = []
                seen_uids: Set[str] = set()
                seen_sql: Set[str] = set()
                target_tables: Set[str] = set()

                def ingest_file(path: Path) -> None:
                    try:
                        with open(path, "r", encoding="utf-8") as f:
                            data = json.load(f)
                    except Exception:
                        return

                    if isinstance(data, dict) and data.get("target_table"):
                        target_tables.add(str(data["target_table"]))

                    if isinstance(data, list):
                        qa_pairs_data = data
                    else:
                        qa_pairs_data = (
                            data.get("qa_pairs", []) if isinstance(data, dict) else []
                        )

                    for qa_dict in qa_pairs_data:
                        try:
                            qa = QAPair.from_dict(qa_dict)
                        except Exception:
                            continue
                        # Ensure every QA pair has a uid
                        qa.ensure_uid()
                        # Dedup by uid first, then by sql as safety net
                        if qa.uid in seen_uids or qa.sql in seen_sql:
                            continue
                        seen_uids.add(qa.uid)
                        seen_sql.add(qa.sql)
                        merged_pairs.append(qa)

                if qa_path.exists():
                    ingest_file(qa_path)
                for candidate in candidate_files:
                    ingest_file(candidate)

                if merged_pairs:
                    qa_dir.mkdir(parents=True, exist_ok=True)
                    strategy_distribution: Dict[str, int] = {}
                    tier_distribution: Dict[str, int] = {}
                    valid_qa_pairs = 0

                    for qa in merged_pairs:
                        if qa.is_valid:
                            valid_qa_pairs += 1
                        strategy_distribution[qa.strategy] = (
                            strategy_distribution.get(qa.strategy, 0) + 1
                        )
                        tier_distribution[qa.tier] = (
                            tier_distribution.get(qa.tier, 0) + 1
                        )

                    target_table_value: Optional[str] = None
                    if len(target_tables) == 1:
                        target_table_value = next(iter(target_tables))

                    merged_data = {
                        "target_table": target_table_value,
                        "total_qa_pairs": len(merged_pairs),
                        "valid_qa_pairs": valid_qa_pairs,
                        "strategy_distribution": strategy_distribution,
                        "tier_distribution": tier_distribution,
                        "qa_pairs": [qa.to_dict() for qa in merged_pairs],
                    }

                    with open(qa_path, "w", encoding="utf-8") as f:
                        json.dump(merged_data, f, indent=2, ensure_ascii=False)

                    logger.info(f"Auto-merged QA pairs into {qa_path}")

        if not qa_path.exists():
            raise FileNotFoundError(f"QA pairs file not found at {qa_path}")

        logger.info(f"Loading QA pairs from {qa_path}")
        with open(qa_path, "r") as f:
            data = json.load(f)

        # Handle both old format (list) and new format (dict with qa_pairs key)
        if isinstance(data, list):
            qa_pairs_data = data
        else:
            qa_pairs_data = data.get("qa_pairs", [])

        qa_pairs = [QAPair.from_dict(qa_dict) for qa_dict in qa_pairs_data]

        # Back-fill UIDs for legacy QA pairs that were saved without one,
        # and persist the updated file so subsequent loads have stable UIDs.
        missing_uid = sum(1 for raw in qa_pairs_data if not raw.get("uid"))
        if missing_uid > 0:
            logger.info(
                f"Back-filling UIDs for {missing_uid}/{len(qa_pairs)} QA pairs "
                f"in {qa_path}"
            )
            for qa in qa_pairs:
                qa.ensure_uid()

            # Re-save the file with UIDs filled in
            if isinstance(data, dict):
                data["qa_pairs"] = [qa.to_dict() for qa in qa_pairs]
            else:
                data = [qa.to_dict() for qa in qa_pairs]

            try:
                with open(qa_path, "w", encoding="utf-8") as f:
                    json.dump(data, f, indent=2, ensure_ascii=False)
                logger.info(f"Persisted {missing_uid} back-filled UIDs to {qa_path}")
            except Exception as e:
                logger.warning(f"Failed to persist back-filled UIDs: {e}")

        logger.info(f"Loaded {len(qa_pairs)} QA pairs")
        return qa_pairs

    def evaluate_qa_pair(
        self,
        qa_pair: QAPair,
        retriever: Any,
        top_k: int = 10,
        mode_name: str = "unknown",
    ) -> EvaluationResult:
        """Evaluate a single QA pair against a retriever.

        Args:
            qa_pair: QA pair to evaluate
            retriever: Retriever instance (must have search method)
            top_k: Number of results to retrieve
            mode_name: Name of the mode being evaluated

        Returns:
            EvaluationResult object
        """
        # Run search
        generated_sql = None
        error_message = None
        result_count = 0
        start_time = time.perf_counter()

        try:
            results = retriever.search(qa_pair.question, top_k=top_k)
        except Exception as e:
            logger.warning(
                f"Search failed for question '{qa_pair.question[:50]}...': {e}"
            )
            results = []
            error_message = str(e)

        end_time = time.perf_counter()
        latency_ms = (end_time - start_time) * 1000

        # Extract predicted row IDs and SQL query
        def extract_row_id(row: dict) -> Optional[str]:
            if not row:
                return None

            key_map = {}
            for k in row.keys():
                if isinstance(k, str):
                    key_map[k.lower()] = k
                else:
                    key_map[str(k).lower()] = k

            preferred_keys = self.id_fields_priority or ["row_id", "id"]
            for preferred in preferred_keys:
                original_key = key_map.get(preferred)
                if original_key is not None and row.get(original_key) is not None:
                    return str(row[original_key])

            for lowered_key, original_key in key_map.items():
                if lowered_key.endswith("_id") and row.get(original_key) is not None:
                    return str(row[original_key])

            for lowered_key, original_key in key_map.items():
                if "number" in lowered_key and row.get(original_key) is not None:
                    return str(row[original_key])

            return None

        predicted_row_ids = set()
        for result in results:
            # Extract SQL query if available (for text2sql mode)
            if hasattr(result, "sql_query") and generated_sql is None:
                generated_sql = result.sql_query
            if hasattr(result, "row_count"):
                result_count = max(result_count, result.row_count)

            # Handle different result types
            if hasattr(result, "row_id"):
                # Standard SearchResult
                predicted_row_ids.add(str(result.row_id))
            elif hasattr(result, "data") and isinstance(result.data, list):
                # Text2SQL result (list of dicts)
                # Try to extract row IDs from data
                for row in result.data:
                    extracted = extract_row_id(row)
                    id_found = extracted is not None
                    if extracted is not None:
                        predicted_row_ids.add(extracted)
                    # If no standard ID field found, try to find primary key column
                    if not id_found and len(row) == 1:
                        # Single column result, assume it's the ID
                        predicted_row_ids.add(str(list(row.values())[0]))
            elif hasattr(result, "data") and isinstance(result.data, dict):
                # Single row result
                row = result.data
                extracted = extract_row_id(row)
                if extracted is not None:
                    predicted_row_ids.add(extracted)

        # Convert expected to set of strings
        expected_row_ids = set(str(rid) for rid in qa_pair.answer_row_ids)

        # Calculate metrics
        set_metrics = compute_set_metrics(expected_row_ids, predicted_row_ids)

        # SQL Metrics
        sql_metrics_results = {}
        if hasattr(qa_pair, "sql") and qa_pair.sql and generated_sql:
            # Check config for enabled metrics
            metrics_config = self.config.get("evaluation", {}).get("metrics", {})
            enabled_metrics = None

            if metrics_config:
                enabled_metrics = []
                metric_keys = [
                    "exact_match",
                    "execution_accuracy",
                    "valid_efficiency_score",
                    "component_matching",
                    "soft_f1",
                    "static_metrics",
                ]
                for key in metric_keys:
                    if metrics_config.get(key, True):
                        enabled_metrics.append(key)

            # Define execution function
            execute_func = None
            need_execution = (
                (enabled_metrics is None)
                or ("execution_accuracy" in enabled_metrics)
                or ("valid_efficiency_score" in enabled_metrics)
            )

            if need_execution and hasattr(retriever, "engine"):

                def _exec(sql):
                    with retriever.engine.connect() as conn:
                        # Ensure we don't fetch too many rows for verification
                        if "limit" not in sql.lower():
                            # Be careful not to modify the query logic if it has ORDER BY
                            # But for verification we might want full results?
                            # Usually limit is fine if we just check first N
                            pass
                        result = conn.execute(text(sql))
                        return [dict(row) for row in result.mappings()]

                execute_func = _exec

            evaluator = SQLEvaluator(execute_sql_func=execute_func)
            try:
                sql_metrics_results = evaluator.evaluate_all(
                    generated_sql,
                    qa_pair.sql,
                    execution_time_ms=latency_ms,
                    enabled_metrics=enabled_metrics,
                )
            except Exception as e:
                logger.warning(f"Failed to calculate SQL metrics: {e}")

        return EvaluationResult(
            question=qa_pair.question,
            expected_row_ids=expected_row_ids,
            predicted_row_ids=predicted_row_ids,
            correct=set_metrics.intersection_size > 0,
            precision=set_metrics.precision,
            recall=set_metrics.recall,
            f1=set_metrics.f1,
            mode_name=mode_name,
            strategy=qa_pair.strategy,
            difficulty_score=qa_pair.difficulty_score,
            generated_sql=generated_sql,
            expected_sql=qa_pair.sql if hasattr(qa_pair, "sql") else None,
            error_message=error_message,
            result_count=result_count,
            latency_ms=latency_ms,
            qa_uid=qa_pair.ensure_uid(),
            evaluated_at=datetime.now().isoformat(),
            exact_match_score=sql_metrics_results.get("exact_match", 0.0),
            execution_accuracy=sql_metrics_results.get("execution_accuracy", 0.0),
            valid_efficiency_score=sql_metrics_results.get(
                "valid_efficiency_score", 0.0
            ),
            component_matching_f1=sql_metrics_results.get("component_matching_f1", 0.0),
            soft_f1=sql_metrics_results.get("soft_f1", 0.0),
            tsed=sql_metrics_results.get("tsed", 0.0),
            sqam=sql_metrics_results.get("sqam", 0.0),
            metric_details=sql_metrics_results,
        )

    def evaluate_mode(
        self,
        mode_name: str,
        qa_pairs: List[QAPair],
        retriever: Any,
        top_k: int = 10,
    ) -> ModeEvaluationSummary:
        """Evaluate a single mode on all QA pairs.

        Args:
            mode_name: Name of the mode
            qa_pairs: List of QA pairs to evaluate
            retriever: Retriever instance
            top_k: Number of results to retrieve per query

        Returns:
            ModeEvaluationSummary object
        """
        logger.info(f"Evaluating mode '{mode_name}' on {len(qa_pairs)} QA pairs")

        results: List[EvaluationResult] = []
        # Use click progress bar for individual QA pairs
        try:
            import click

            with click.progressbar(
                qa_pairs,
                label=f"  Evaluating {mode_name}",
                length=len(qa_pairs),
                show_percent=True,
                show_pos=True,
                item_show_func=lambda x: (
                    f"Question: {x.question[:60]}..." if x else None
                ),
            ) as bar:
                for qa_pair in bar:
                    result = self.evaluate_qa_pair(qa_pair, retriever, top_k, mode_name)
                    results.append(result)
        except ImportError:
            # Fallback if click is not available
            for i, qa_pair in enumerate(qa_pairs):
                if (i + 1) % 10 == 0:
                    logger.info(f"  Processing {i + 1}/{len(qa_pairs)}...")
                result = self.evaluate_qa_pair(qa_pair, retriever, top_k, mode_name)
                results.append(result)

        # Aggregate into a ModeEvaluationSummary
        return self._build_summary_from_results(mode_name, results)

    def evaluate_all_modes(
        self,
        mode_names: List[str],
        retrievers: Dict[str, Any],
        qa_pairs: List[QAPair],
        top_k: int = 10,
    ) -> Dict[str, ModeEvaluationSummary]:
        """Evaluate multiple modes and return summary.

        Args:
            mode_names: List of mode names to evaluate
            retrievers: Dict mapping mode_name -> retriever instance
            qa_pairs: List of QA pairs to evaluate
            top_k: Number of results to retrieve per query

        Returns:
            Dict mapping mode_name -> ModeEvaluationSummary
        """
        summaries = {}

        # Evaluate each mode (progress bar is shown within evaluate_mode for QA pairs)
        for mode_name in mode_names:
            if mode_name not in retrievers:
                logger.warning(f"Retriever not found for mode '{mode_name}', skipping")
                continue

            retriever = retrievers[mode_name]
            summary = self.evaluate_mode(mode_name, qa_pairs, retriever, top_k)
            summaries[mode_name] = summary

        return summaries

