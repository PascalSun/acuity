    def _html_best_modes(
        self,
        modes_summary: Dict[str, Dict[str, Any]],
        metric_key: str,
        *,
        higher_is_better: bool,
        require_sql: bool = False,
    ) -> Set[str]:
        values: list[tuple[str, float]] = []
        for mode_name, mode_data in modes_summary.items():
            if require_sql and int(mode_data.get("sql_evaluated_questions", 0)) <= 0:
                continue
            raw = mode_data.get(metric_key)
            if raw is None:
                continue
            try:
                values.append((mode_name, float(raw)))
            except Exception:
                continue

        if not values:
            return set()

        best_value = (
            max(v for _, v in values) if higher_is_better else min(v for _, v in values)
        )
        tol = 1e-12
        return {m for m, v in values if abs(v - best_value) <= tol}

