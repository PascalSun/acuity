@dataclass
class EvaluationResult:
    """Evaluation result for a single QA pair."""

    question: str
    expected_row_ids: Set[Union[int, str]]
    predicted_row_ids: Set[Union[int, str]]
    correct: bool
    precision: float
    recall: float
    f1: float
    mode_name: str
    strategy: Optional[str] = None
    difficulty_score: Optional[float] = None
    # Additional fields for detailed analysis
    generated_sql: Optional[str] = None  # SQL query generated by the retriever
    expected_sql: Optional[str] = None  # Expected SQL from QA pair
    error_message: Optional[str] = None  # Error message if execution failed
    result_count: int = 0  # Number of rows returned
    latency_ms: float = 0.0  # Execution time in milliseconds

    # QA pair identity and evaluation timestamp
    qa_uid: Optional[str] = None  # Stable QA pair UID (from QAPair.uid)
    evaluated_at: Optional[str] = None  # ISO timestamp of when this result was produced

    # New SQL Metrics
    exact_match_score: float = 0.0
    execution_accuracy: float = 0.0
    valid_efficiency_score: float = 0.0
    component_matching_f1: float = 0.0
    soft_f1: float = 0.0
    tsed: float = 0.0
    sqam: float = 0.0
    metric_details: Dict[str, Any] = field(default_factory=dict)

    @property
    def is_exact_match(self) -> bool:
        """Whether prediction exactly matches expected."""
        return self.expected_row_ids == self.predicted_row_ids

    @property
    def intersection_size(self) -> int:
        """Number of correctly predicted row IDs."""
        return len(self.expected_row_ids & self.predicted_row_ids)


@dataclass
class RetrievalMetricsSummary:
    hit_rate: float
    exact_match_rate: float
    micro_precision: float
    micro_recall: float
    micro_f1: float
    macro_precision: float
    macro_recall: float
    macro_f1: float
    expected_rows: int
    predicted_rows: int
    correct_rows: int


@dataclass
class SQLMetricsSummary:
    evaluated_questions: int = 0
    coverage: float = 0.0
    avg_exact_match: float = 0.0
    avg_execution_accuracy: float = 0.0
    avg_valid_efficiency_score: float = 0.0
    avg_component_matching_f1: float = 0.0
    avg_soft_f1: float = 0.0
    avg_tsed: float = 0.0
    avg_sqam: float = 0.0


@dataclass
class LatencyMetricsSummary:
    mean_ms: float = 0.0
    median_ms: float = 0.0
    min_ms: float = 0.0
    max_ms: float = 0.0
    p95_ms: float = 0.0
    p99_ms: float = 0.0


@dataclass
class ModeEvaluationSummary:
    """Summary statistics for a mode's evaluation."""

    mode_name: str
    total_questions: int
    exact_matches: int
    hit_questions: int
    retrieval_metrics: RetrievalMetricsSummary
    sql_metrics: SQLMetricsSummary = field(default_factory=SQLMetricsSummary)
    latency: LatencyMetricsSummary = field(default_factory=LatencyMetricsSummary)

    breakdown_by_strategy: Dict[str, Dict[str, Any]] = field(default_factory=dict)
    breakdown_by_tier: Dict[str, Dict[str, Any]] = field(default_factory=dict)
    # Detailed results for each QA pair
    detailed_results: List[EvaluationResult] = field(default_factory=list)


