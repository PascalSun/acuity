    def _html_render_tab_contents(
        self,
        modes_evaluated: List[str],
        detailed_results: Dict[str, List[Dict[str, Any]]],
        summaries: Dict[str, ModeEvaluationSummary],
    ) -> str:
        html_parts: list[str] = []
        for idx, mode_name in enumerate(modes_evaluated):
            active = "active" if idx == 0 else ""
            mode_results = detailed_results.get(mode_name, [])

            html_parts.append(
                f'<div id="tab-{mode_name}" class="tab-content {active}">'
            )
            html_parts.append(f"<h2>{mode_name} - Detailed Results</h2>")

            summary = summaries.get(mode_name)
            if summary:
                html_parts.append(self._html_render_mode_panels(summary))

            html_parts.append(self._html_render_filter_bar(mode_name))
            html_parts.append(
                self._html_render_strategy_options_script(
                    mode_name, self._html_collect_strategies(mode_results)
                )
            )
            html_parts.append(self._html_render_results_table(mode_name, mode_results))
            html_parts.append("</div>")

        return "".join(html_parts)

    def _html_render_config_sections(self) -> str:
        base_config_path = get_base_config_path()
        loaded_config_path = get_loaded_config_path()

        html_parts: list[str] = []
        for title, path in (
            ("Base Configuration (config.yml)", base_config_path),
            ("Run Configuration", loaded_config_path),
        ):
            if path is None:
                continue
            if base_config_path and loaded_config_path:
                try:
                    if (
                        title == "Run Configuration"
                        and path.resolve() == base_config_path.resolve()
                    ):
                        continue
                except Exception:
                    pass

            config_content = self._get_config_file_content(path, redact=True)
            if not config_content:
                continue

            html_parts.append('<div class="config-section">')
            html_parts.append(
                f"<h2>{html.escape(title)} ({html.escape(str(path))})</h2>"
            )
            html_parts.append(
                f'<div class="config-content">{html.escape(config_content)}</div>'
            )
            html_parts.append("</div>")

        return "".join(html_parts)

