    def _get_model_info(self, mode_names: List[str]) -> Dict[str, str]:
        """Get model information for agent-based modes.

        Args:
            mode_names: List of mode names that were evaluated

        Returns:
            Dict mapping mode_name -> model info string
        """
        model_info = {}

        # Check which modes use agents (e.g., text2sql modes)
        agent_based_modes = [name for name in mode_names if "text2sql" in name.lower()]

        if agent_based_modes:
            global_agent = self.config.get("agent", {}) or {}
            modes_cfg = self.config.get("modes", {}) or {}

            for mode_name in agent_based_modes:
                # Merge global agent config with mode-specific override (same as Text2SQL init)
                mode_block = (
                    modes_cfg.get(mode_name, {}) if isinstance(modes_cfg, dict) else {}
                )
                mode_agent = (
                    mode_block.get("agent", {}) if isinstance(mode_block, dict) else {}
                )
                agent_cfg = (
                    {**global_agent, **mode_agent} if mode_agent else dict(global_agent)
                )

                provider = agent_cfg.get("provider", "unknown")
                model = agent_cfg.get("model") or agent_cfg.get(provider, {}).get(
                    "model"
                )

                model_info[mode_name] = (
                    f"{provider}/{model}" if model else f"{provider}/default"
                )

        return model_info

    def _redact_config_dict(self, obj: Any, *, in_keys_block: bool = False) -> Any:
        def mask_value(value: Any) -> Any:
            if value is None:
                return None
            if isinstance(value, str):
                if value.startswith("${") and value.endswith("}"):
                    return "${***}"
                s = value.strip()
                if len(s) <= 8:
                    return "***"
                return f"{s[:2]}***{s[-2:]}"
            return "***"

        if isinstance(obj, dict):
            redacted: Dict[str, Any] = {}
            for k, v in obj.items():
                k_str = str(k)
                k_lower = k_str.lower()
                is_sensitive_key = any(
                    token in k_lower
                    for token in (
                        "api_key",
                        "apikey",
                        "access_key",
                        "private_key",
                        "secret",
                        "token",
                        "password",
                        "passwd",
                        "keys",
                    )
                ) or k_lower.endswith("_key")

                if k_lower == "keys":
                    redacted[k_str] = self._redact_config_dict(v, in_keys_block=True)
                elif in_keys_block or is_sensitive_key:
                    if isinstance(v, dict):
                        redacted[k_str] = self._redact_config_dict(
                            v, in_keys_block=True
                        )
                    elif isinstance(v, list):
                        redacted[k_str] = [mask_value(x) for x in v]
                    else:
                        redacted[k_str] = mask_value(v)
                else:
                    redacted[k_str] = self._redact_config_dict(v, in_keys_block=False)
            return redacted

        if isinstance(obj, list):
            return [
                self._redact_config_dict(x, in_keys_block=in_keys_block) for x in obj
            ]

        return obj

    def _get_config_file_content(
        self, config_path: Path, *, redact: bool = False
    ) -> Optional[str]:
        if not config_path.exists():
            return None

        try:
            raw = config_path.read_text(encoding="utf-8")
            if not redact:
                return raw

            try:
                parsed = yaml.safe_load(raw)
                redacted = self._redact_config_dict(parsed)
                return yaml.safe_dump(
                    redacted, sort_keys=False, default_flow_style=False
                )
            except Exception:
                return raw
        except Exception as e:
            logger.warning(f"Failed to read config file ({config_path}): {e}")
            return None

