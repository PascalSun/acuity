    def merge_evaluation_runs(
        self,
        evaluation_files: List[Path],
        *,
        output_path: Optional[Path] = None,
        modes: Optional[List[str]] = None,
    ) -> Path:
        """Merge multiple evaluation JSON files and generate a unified HTML.

        For every ``(mode, qa_uid)`` pair that appears in more than one file,
        the result with the **latest** ``evaluated_at`` timestamp wins.  This
        allows incremental benchmark runs: you can re-evaluate a single mode
        and the merge will pick up the newest data automatically.

        After merging, we compute the **intersection** of QA pair UIDs across
        all selected modes so each mode is compared on exactly the same
        question set.

        Args:
            evaluation_files: List of paths to evaluation JSON files.
            output_path: Optional path for the merged HTML.  If *None*, uses
                the benchmark directory and writes ``merged_evaluation.html``.
            modes: Optional list of mode names to include.  If *None*, every
                mode found in the input files is included.

        Returns:
            Path to the generated HTML file.
        """
        import hashlib

        if not evaluation_files:
            raise ValueError("No evaluation files provided for merge_evaluation_runs()")

        files_sorted = sorted(Path(p) for p in evaluation_files)

        # per_mode_results: mode_name -> uid -> (evaluated_at, EvaluationResult)
        per_mode_results: Dict[str, Dict[str, tuple]] = {}

        for file_path in files_sorted:
            try:
                with open(file_path, "r", encoding="utf-8") as f:
                    data = json.load(f)
            except Exception as e:
                logger.warning(f"Failed to load evaluation file {file_path}: {e}")
                continue

            # File-level timestamp used as fallback for old JSONs
            file_timestamp = data.get("timestamp")

            detailed = data.get("detailed_results") or {}
            if not isinstance(detailed, dict):
                continue

            for mode_name, results_list in detailed.items():
                if modes is not None and mode_name not in modes:
                    continue

                mode_store = per_mode_results.setdefault(mode_name, {})
                if not isinstance(results_list, list):
                    continue

                for item in results_list:
                    if not isinstance(item, dict):
                        continue

                    result = self._evaluation_result_from_json(
                        mode_name, item, file_timestamp=file_timestamp
                    )

                    uid = result.qa_uid
                    if not uid:
                        # Last-resort fallback: hash question text
                        q = str(item.get("question", "")).strip()
                        if not q:
                            continue
                        uid = hashlib.sha256(q.encode("utf-8")).hexdigest()[:16]
                        result.qa_uid = uid

                    ts = result.evaluated_at or ""

                    # Keep the result with the latest evaluated_at for each uid
                    existing = mode_store.get(uid)
                    if existing is None or ts >= existing[0]:
                        mode_store[uid] = (ts, result)

        if not per_mode_results:
            raise ValueError("No evaluation results found in the provided files")

        # Determine which modes to keep
        all_modes_found = sorted(per_mode_results.keys())
        if modes is not None:
            selected_modes = [m for m in all_modes_found if m in modes]
        else:
            selected_modes = all_modes_found

        if not selected_modes:
            raise ValueError("No matching modes found in evaluation files")

        # Compute intersection of UIDs across all selected modes
        uid_sets: Dict[str, set] = {}
        for mode_name in selected_modes:
            uid_sets[mode_name] = set(per_mode_results[mode_name].keys())

        # All modes must have at least one UID
        non_empty = [s for s in uid_sets.values() if s]
        if not non_empty:
            raise ValueError("No questions available to merge across modes")

        intersect_uids = set.intersection(*non_empty)

        if not intersect_uids:
            # Build a helpful diagnostic message
            diag_parts = []
            for m in selected_modes:
                diag_parts.append(f"  {m}: {len(uid_sets.get(m, set()))} questions")
            diag = "\n".join(diag_parts)
            raise ValueError(
                f"No common QA pair UIDs found across the {len(selected_modes)} "
                f"selected modes; cannot create merged evaluation.\n"
                f"Per-mode question counts:\n{diag}"
            )

        logger.info(
            "Merge: %d modes, %d UIDs in intersection (per-mode totals: %s)",
            len(selected_modes),
            len(intersect_uids),
            ", ".join(f"{m}={len(uid_sets.get(m, set()))}" for m in selected_modes),
        )

        # Rebuild per-mode summaries restricted to the intersected UIDs
        merged_summaries: Dict[str, ModeEvaluationSummary] = {}
        for mode_name in selected_modes:
            mode_store = per_mode_results[mode_name]
            mode_results = [
                mode_store[uid][1] for uid in intersect_uids if uid in mode_store
            ]
            if not mode_results:
                continue
            merged_summaries[mode_name] = self._build_summary_from_results(
                mode_name, mode_results
            )

        if not merged_summaries:
            raise ValueError("Failed to build summaries for any mode after merging")

        qa_count = len(intersect_uids)

        # Decide output path -- merged reports go in a dedicated subfolder
        if output_path is None:
            # Place merged HTMLs under <benchmark_dir>/merged/
            # Derive benchmark_dir from the common parent of all input files
            benchmark_dir = files_sorted[0].parent
            # Walk up if we're inside a run_* subdirectory
            if benchmark_dir.name.startswith("run_"):
                benchmark_dir = benchmark_dir.parent
            merged_dir = benchmark_dir / "merged"
            merged_dir.mkdir(parents=True, exist_ok=True)
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            mode_tag = "_".join(sorted(selected_modes))
            if len(mode_tag) > 60:
                mode_tag = f"{len(selected_modes)}_modes"
            output_path = merged_dir / f"merged_{mode_tag}_{timestamp}.html"

        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Generate HTML visualization
        self.generate_html_visualization(merged_summaries, qa_count, output_path)
        logger.info(
            "Merged evaluation HTML generated at %s "
            "(%d modes, %d intersected questions)",
            output_path,
            len(merged_summaries),
            qa_count,
        )
        return output_path

